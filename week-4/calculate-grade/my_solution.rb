def get_grade(percentage)

    if percentage  >= 90
     return "A"

    elsif percentage >= 80 && percentage <90
      return "B"

     elsif percentage >= 70 && percentage <80
      return "C"

     elsif percentage >= 60 && percentage <70
      return "D"

     else percentage <= 50
      return "F"
  end

end








